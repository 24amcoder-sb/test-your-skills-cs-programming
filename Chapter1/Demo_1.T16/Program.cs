Console.WriteLine("***Illustration: do-while loop***");
int j = 1;
do
{
    Console.WriteLine($"Inside loop: j is now {j}");
    j++;
} while (j < 1);
Console.WriteLine($"Outside loop: j's final value is {j}");

Console.WriteLine("\n***Illustration:while loop***");
j = 1;
while (j < 1)
{
    Console.WriteLine($"Inside loop: j is now {j}");
    j++;
};
Console.WriteLine($"Outside loop: j's final value is {j}");

Console.WriteLine("\n---End of 1.T16---");

